# Variables are used to store values so we can refer to them later.
# A variable is like a label, and you use the '=' symbol, known as
# the assignment operator, to assign a value to a variable.

a = 10
b = c = 20  # This is called a "chained assignment" b = 20, c = 20

print(a)
print(b)
print(c)

greetings = "Hola, como estas? muy bien, y tu?"
print(greetings)

